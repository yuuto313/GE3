#include "Framework.h"

void Framework::Initialize()
{
}

void Framework::Finalize()
{
}

void Framework::Update()
{
}

void Framework::Run()
{

	//-------------------------------------
	// ゲームの初期化
	//-------------------------------------

	Initialize();

	//-------------------------------------
	// ゲームループ
	//-------------------------------------

	while (true)
	{
		//-------------------------------------
		// 毎フレーム更新処理
		//-------------------------------------

		Update();

		//-------------------------------------
		// 終了リクエストが来たら抜ける
		//-------------------------------------

		if (IsEndRequest()) {
			break;
		}

		//-------------------------------------
		// 描画
		//-------------------------------------

		Draw();

	}

	//-------------------------------------
	// ゲームの終了
	//-------------------------------------

	Finalize();

}
